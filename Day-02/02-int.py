"""
This is to perform basic Arithmetic operations on float numbers
"""

#Integer variables

num1 = 10
num2 = 5

#Integral Division
result1 = num1 // num2
print("Integer division:", result1)

#Modulus or Reminder
result2 = num1 % num2
print("Modulus Reminder:", result2)

#Absolute Value
result3 = abs(-7)
print("Absolute value: ", result3)
