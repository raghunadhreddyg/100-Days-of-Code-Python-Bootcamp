"""
This is to perform basic Arithmetic operations on float numbers.
"""

#Float Variables

num1 = 5.0
num2 = 3.5

# Basic Arithmetic operations

result1 = num1 + num2
print("The addition of given two numbers is", result1)

result2 = num1 - num2
print("The subtraction of given two numbers is", result2)

result3 = num1 * num2
print("The multiplication of given two numbers is", result3)

result4 = num1 / num2
print("The division of given two numbers is", result4)

#Rounding numbers to particular decimal places
result5 = round(5.1324563, 2)  #Rounds to 2 decimal places
print("The rounded of number is", result5)
